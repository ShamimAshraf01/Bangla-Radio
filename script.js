function updateMediaSession(st) {
    if (!("mediaSession" in navigator)) return;
    navigator.mediaSession.metadata = new MediaMetadata({
        title: st.n,
        artist: st.f,
        album: "Bangla Radio",
        artwork: [
            { src: "icon-192.png", sizes: "192x192", type: "image/png" },
            { src: "icon-512.png", sizes: "512x512", type: "image/png" }
        ]
    });
    navigator.mediaSession.setActionHandler("play", function(){if(audio)audio.play();});
    navigator.mediaSession.setActionHandler("pause", function(){stopAll();});
    navigator.mediaSession.setActionHandler("stop", function(){stopAll();});
    navigator.mediaSession.setActionHandler("previoustrack", function(){switchStation(-1);});
    navigator.mediaSession.setActionHandler("nexttrack", function(){switchStation(1);});
}

function clearConnectTimer() { if(connectTimer){clearInterval(connectTimer);connectTimer=null;} connectSec=0; }

function playStation(st, card) {
    if (curStation && curStation.n===st.n && curStation.s===st.s) { stopAll(); return; }
    stopAll();
    curStation = st; curCard = card;
    if(card) card.classList.add("loading");
    saveLastPlayed(st);
    setPlayer("সংযোগ হচ্ছে...", st.n, true, true);

    connectSec = 0;
    connectTimer = setInterval(function() {
        connectSec++;
        if (audio && curStation === st) {
            if (connectSec < 15) { setPlayer("সংযোগ হচ্ছে " + connectSec + "s", st.n, true, true); }
            else {
                clearConnectTimer();
                if(audio){audio.pause();audio.src="";audio=null;}
                if(card) card.classList.remove("loading","playing");
                setPlayer("ধীর নেটওয়ার্ক","",true,true);
                setTimeout(function(){if(!curStation)setPlayer("","",false,false);},3000);
                curStation=null;curCard=null;
            }
        } else clearConnectTimer();
    }, 1000);

    audio = new Audio();
    audio.preload = "none";
    audio.volume = document.getElementById("volSlider").value / 100;

    // Low-end device optimization: minimal memory, no unnecessary buffering
    if (audio.mozAutoplayPolicy !== undefined) audio.mozAutoplayPolicy = "allowed";

    var a = audio, retry = 0;

    function onReady() {
        if(audio!==a) return; clearConnectTimer();
        if(card){card.classList.remove("loading");card.classList.add("playing");}
        setPlayer(st.n, st.f, true, false);
        updateMediaSession(st);
    }

    a.addEventListener("canplay", function h() { a.removeEventListener("canplay",h); onReady(); });
    a.addEventListener("playing", function() { onReady(); });

    a.addEventListener("error", function() {
        if(audio!==a) return;
        if(retry<2){retry++;setPlayer("চেষ্টা "+retry+"/2",st.n,true,true);setTimeout(function(){if(audio!==a)return;a.load();a.play().catch(function(){});},2000);return;}
        clearConnectTimer();
        if(card)card.classList.remove("loading","playing");
        setPlayer("সংযোগ হয়নি","",true,true);
        setTimeout(function(){if(!curStation)setPlayer("","",false,false);},3000);
        curStation=null;curCard=null;audio=null;
    });

    a.addEventListener("waiting", function() {
        if(audio!==a)return; if(card)card.classList.add("loading");
        setPlayer("বাফারিং...", st.n, true, true);
    });

    // Auto-reconnect if stream drops (common on BD networks)
    a.addEventListener("stalled", function() {
        if(audio!==a) return;
        setPlayer("পুনঃসংযোগ...", st.n, true, true);
        setTimeout(function(){
            if(audio===a && a.paused) { a.load(); a.play().catch(function(){}); }
        }, 3000);
    });

    a.src = st.s;
    a.play().catch(function(){
        if(audio!==a)return;clearConnectTimer();
        if(card)card.classList.remove("loading","playing");
        setPlayer("প্লে হচ্ছে না","",true,true);
        setTimeout(function(){if(!curStation)setPlayer("","",false,false);},3000);
        curStation=null;curCard=null;audio=null;
    });
}

function stopAll() {
    clearConnectTimer();
    if(audio){
        audio.pause();
        audio.removeAttribute("src");
        audio.load(); // release network connection and memory
        audio=null;
    }
    if(curCard){curCard.classList.remove("playing","loading");curCard=null;}
    curStation=null; clearSleepTimer();
    setPlayer("","",false,false);
}

